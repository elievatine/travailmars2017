#include <stdio.h>

int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	while (nb > (1 * i))
	{
		i++;
	}
	if ((nb % i) != 0)
		i = 0;
	return (i);
}

int		main(void)
{
	printf("%d", ft_sqrt(8));

	return(0);
}
